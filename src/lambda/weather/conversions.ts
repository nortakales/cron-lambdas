export function mmToIn(mm: number) {
    return mm / 25.4;
}

export function cmToIn(cm: number) {
    return cm / 2.54;
}

export function mToMi(m: number) {
    return m / 1609.344;
}

export function cToF(c: number) {
    return c * (9 / 5) + 32;
}

export function kmhToMph(kmh: number) {
    return kmh / 1.609344;
}